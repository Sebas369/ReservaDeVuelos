    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        camposObligatorios();
        if (!jTextFieldModNumero.getText().equals("") && !jTextFieldModPrecio.getText().equals("")) {
            AsientoData ad = new AsientoData(conexion);
            Asiento unAsiento;
            int idA = Integer.parseInt(jComboBoxModIdAsiento.getSelectedItem().toString());
            int idV = Integer.parseInt(jComboBoxModIdVuelo.getSelectedItem().toString());
            String letra = jComboBoxModLetra.getSelectedItem().toString();
            int numero = Integer.parseInt(jTextFieldModNumero.getText());
            boolean disp = jRadioButtonModDisp.isSelected();
            double precio = Double.parseDouble(jTextFieldModPrecio.getText());
            unAsiento = new Asiento(idV, letra, numero, disp, precio);
            ad.modificarAsiento(unAsiento,idA);
            Inicio.escritorio.updateUI();
            this.dispose();
            
        } else {
            JOptionPane.showMessageDialog(null, "Faltan campos obligatorios");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

