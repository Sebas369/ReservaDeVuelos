    public VistaModificarAsiento() {
        initComponents();
        
        try {
             conexion = new Conexion("jdbc:mysql://localhost/reserva_de_vuelos", "root", "");
         } catch (ClassNotFoundException ex) {
             Logger.getLogger(VistaAltaVuelo.class.getName()).log(Level.SEVERE, null, ex);
         }
        jComboBoxModIdAsiento.removeAllItems();
        jComboBoxModIdVuelo.removeAllItems();
        
        try {
            connection = conexion.getConexion();
            String sql = " SELECT * FROM vuelo ";
            String sql2 = " SELECT * FROM asiento ";
            PreparedStatement statement = connection.prepareStatement(sql);
            
            ResultSet resultSet = statement.executeQuery();
            while(resultSet.next()){
                String id = String.valueOf(resultSet.getInt("idVuelo"));
                jComboBoxModIdVuelo.addItem(id);                
            }
            statement.close();
            PreparedStatement statement2 = connection.prepareStatement(sql2);
            ResultSet resultSet2 = statement2.executeQuery();
            while(resultSet2.next()){
                String idA = String.valueOf(resultSet2.getInt("idAsiento"));
                jComboBoxModIdAsiento.addItem(idA);                
            }
            
            statement2.close();
            
        } catch (SQLException ex) {
            System.out.println("Error al obtener la conexion");
        }
        jLabelCampoO1.setVisible(false);
        jLabelCampoO2.setVisible(false);
        
    }
