    public void mostrar(){        
        AsientoData ad = new AsientoData(conexion);
        List<Asiento> asientos = new ArrayList<>();
        asientos = ad.obtenerAsientos();
        modelo = (DefaultTableModel) jTableAsientos.getModel();
        borrarFilas();
        Object[] fila = new Object [modelo.getColumnCount()]; 
        for (int i = 0; i < asientos.size(); i++){
            
            fila[0] = asientos.get(i).getIdAsiento();
            fila[1]=asientos.get(i).getIdVuelo();
            fila[2]=asientos.get(i).getLetra();
            fila[3]=asientos.get(i).getNumero();
            fila[4]=asientos.get(i).getPrecioAsiento();

            modelo.addRow(fila);
        }
        
       jTableAsientos.setModel(modelo);
                
    }
    public void borrarFilas(){
        int a = modelo.getRowCount()-1;
        for(int i = a; i >= 0; i--){
                modelo.removeRow(i);
            }
    }

