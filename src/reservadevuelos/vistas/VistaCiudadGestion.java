    //carga los datos de la lista en la tabla
     private void mostrar(List <Ciudad> ciudades){        
        modelo = (DefaultTableModel) jTableCiudades.getModel();
        borrarFilas();
        Object[] fila= new Object [modelo.getColumnCount()]; 
        for (int i=0;i < ciudades.size();i++){
            fila[0]=ciudades.get(i).getIdCiudad();
            fila[1]=ciudades.get(i).getNombreCiudad();            
            fila[2]=ciudades.get(i).getPais();
            if (ciudades.get(i).getVigencia()){
            fila[3]="Vigente";}
            else{
                fila[3]="Fuera de servicio";
            }
            modelo.addRow(fila);
        }
        
       jTableCiudades.setModel(modelo);
    }
    
    // borra las filas de la tabla
     public void borrarFilas(){
        int a =modelo.getRowCount()-1;
        for(int i=a;i>=0;i--){
                modelo.removeRow(i );
            }
    }
    
