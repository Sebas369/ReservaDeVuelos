    private void jTableCiudadesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableCiudadesMouseClicked
        habilitarCampos();
        jRBVigencia.setSelected(false);
        int selection=jTableCiudades.rowAtPoint(evt.getPoint());
        jTextId.setText(String.valueOf(jTableCiudades.getValueAt(selection, 0)));
        jTextNombreCiudad.setText(String.valueOf(jTableCiudades.getValueAt(selection,1)));
        jTextPais.setText(String.valueOf(jTableCiudades.getValueAt(selection,2)));
        if (jTableCiudades.getValueAt(selection,3)=="Vigente"){
            jRBVigencia.setSelected(true);
        }
        jTextId.setEditable(false);
    }//GEN-LAST:event_jTableCiudadesMouseClicked

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (JOptionPane.showConfirmDialog(null, "¿Seguro que desea eliminar el asiento?") == JOptionPane.YES_OPTION){
            cd= new CiudadData(conexion);
            ciudad1=new Ciudad(jTextNombreCiudad.getText(),jTextPais.getText(),jRBVigencia.isSelected());
            ciudad1.setIdCiudad(Integer.parseInt(jTextId.getText()));
            cd.modifCiudad(ciudad1);
            mostrar(cd.obtenerCiudades());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

